#include <string.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

#include <stdio.h>

int	main(void)
{
	char *source;
	char destination[50];

	source = "Hey there";
	ft_strcpy(destination, source);
	printf("%s\n", destination);
}
